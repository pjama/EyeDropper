	public String getClosestColor(int r, int g, int b) {
		Point3D refPoint = new Point3D(r, g, b, null);
		Point3D closestPoint = null;
		double closestDistance = Double.MAX_VALUE;
		for(Map.Entry<String, Point3D> entry : this.ralColors.entrySet()) {
			Point3D p = entry.getValue();
			double distance = p.getDistance(refPoint);
			if (distance < closestDistance) {
				closestDistance = distance;
				closestPoint = p;
			}
		}
		if (closestPoint == null) return null;
		return closestPoint.getName();
	}
	
